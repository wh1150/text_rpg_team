// м§Җм •н•ң мң„м№ҳм—җ л°•мҠӨ н…Ңл‘җлҰ¬лҘј к·ёлҰј
// x, y : л°•мҠӨ мӢңмһ‘ мң„м№ҳ (мўҢмғҒлӢЁ)
// width : л°•мҠӨ к°ҖлЎң кёёмқҙ
// height : л°•мҠӨ м„ёлЎң кёёмқҙ
// ==========================================
void DrawBox(int x, int y, int width, int height)
{
    // мғҒлӢЁ н…Ңл‘җлҰ¬ м¶ңл Ҙ
    Gotoxy(x, y);
    cout << "в”Ң";
    for (int i = 0; i < width - 2; i++) { cout << "в”Җ"; }
    cout << "в”җ";

    // мўҢмҡ° лІҪ м¶ңл Ҙ
    for (int i = 1; i < height - 1; i++) {
        Gotoxy(x, y + i);
        cout << "в”Ӯ";  // мҷјмӘҪ лІҪ

        Gotoxy(x + width - 1, y + i);
        cout << "в”Ӯ";  // мҳӨлҘёмӘҪ лІҪ
    }

    // н•ҳлӢЁ н…Ңл‘җлҰ¬ м¶ңл Ҙ
    Gotoxy(x, y + height - 1);
    cout << "в””";
    for (int i = 0; i < width - 2; i++) { cout << "в”Җ"; }
    cout << "в”ҳ";
}

