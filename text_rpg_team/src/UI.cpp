// м»Өм„ңлҘј мҪҳмҶ”мқҳ (x, y) мң„м№ҳлЎң мқҙлҸҷ
// x : к°ҖлЎң мң„м№ҳ, y : м„ёлЎң мң„м№ҳ
// ==========================================
void Gotoxy(int x, int y)
{
	// COORDлҠ” Windows APIм—җм„ң мўҢн‘ңлҘј лӮҳнғҖлӮҙлҠ” кө¬мЎ°мІҙ.
    // Windows APIмқҳ COORDлҠ” SHORT нғҖмһ…мқ„ мӮ¬мҡ©.
	// x = к°ҖлЎң мң„м№ҳ, y = м„ёлЎң мң„м№ҳлҘј лӮҳнғҖлғ„.
    COORD pos = { (SHORT)x, (SHORT)y };


    // мҪҳмҶ” м¶ңл Ҙм°Ҫ (STD_OUTPUT_HANDLE)мқҳ н•ёл“Өмқ„ к°Җм ёмҳЁ нӣ„
    // м»Өм„ңлҘј pos мң„м№ҳлЎң мқҙлҸҷ.
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), pos);
}


